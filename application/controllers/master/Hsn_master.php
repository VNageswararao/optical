 }
	public function index()
	{
      $data['title']='Pharmacy::Hsn Master';
      $data['activecls']='Hsn_master';
      $var_array=array($this->session->office_id);
      $data['tax']=$this->Item_model->gettax($var_array);
      $content=$this->load->view('master/hsn_master',$data,true);
      $this->load->view('includes/layout',['content'=>$content]);
	}
    private function fetch_data() 
    {
    	$status=trim($this->input->post('status'));
        if(!$this->input->post('status'))
        {
            $status=0;
        }
        $office_id=$this->session->office_id;
        return array(
            'hsn_code'=>trim($this->input->post('hsn_code')),
            'tax_id'=>trim($this->input->post('tax_id')),
            'description'=>trim($this->input->post('description')),
            'status'=>$status,
            'login_id'=>$this->session->userdata('login_id'),
            'office_id'=> $this->session->userdata('office_id')
           );
    }

